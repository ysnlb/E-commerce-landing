export const DEFAULT_THEME_ID = 'warm'

export function resolveTheme(themeId) {
  return THEMES[themeId] ?? THEMES[DEFAULT_THEME_ID]
}

// Inline style object for the canvas root: CSS custom properties consumed
// by the template components, plus the base font/background/text color.
export function themeVars(theme) {
  return {
    '--t-canvas': theme.canvas,
    '--t-ink': theme.ink,
    '--t-ink-soft': theme.inkSoft,
    '--t-tile': theme.tile,
    '--t-tile-soft': theme.tileSoft,
    '--t-strike': theme.strike,
    '--t-seal': theme.seal,
    '--t-cta-from': theme.ctaFrom,
    '--t-cta-to': theme.ctaTo,
    '--t-cta-shadow': theme.ctaShadow,
    '--t-glow': theme.glow,
    '--t-card': theme.card,
    '--t-card-border': theme.cardBorder,
    '--t-display': theme.display,
    fontFamily: theme.body,
    backgroundColor: theme.canvas,
    color: theme.ink,
  }
}
